package com.spring.nursery;


public class FinalVO {
	
	private int gid;
	private int per_cnt_score;
	private int child_rate;
	private int distance_rate;
	private int djg_child_rate;
	private float national_rate;
	private String geom;
	
	public int getGid() {
		return gid;
	}
	public void setGid(int gid) {
		this.gid = gid;
	}
	public int getPer_cnt_score() {
		return per_cnt_score;
	}
	public void setPer_cnt_score(int per_cnt_score) {
		this.per_cnt_score = per_cnt_score;
	}
	public int getChild_rate() {
		return child_rate;
	}
	public void setChild_rate(int child_rate) {
		this.child_rate = child_rate;
	}
	public int getDistance_rate() {
		return distance_rate;
	}
	public void setDistance_rate(int distance_rate) {
		this.distance_rate = distance_rate;
	}
	public float getNational_rate() {
		return national_rate;
	}
	public void setNational_rate(float national_rate) {
		this.national_rate = national_rate;
	}
	public String getGeom() {
		return geom;
	}
	public void setGeom(String geom) {
		this.geom = geom;
	}
	public int getDjg_child_rate() {
		return djg_child_rate;
	}
	public void setDjg_child_rate(int djg_child_rate) {
		this.djg_child_rate = djg_child_rate;
	}


}
